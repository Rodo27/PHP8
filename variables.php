/*

    ¿Qué es una variable?

        Es el espacio de memoria que utilizamos para almacenar datos que utilizaremos de manera repetida en nuestra aplicación.

        Igual que en otros lenguajes de programación, en PHP utilizamos identificadores para definir una variable.

        Ejem: $note = 20;



    Características de las variables

        El nombre de las variables siempre debe empezar con el signo $

        El nombre de las variables es sensible a mayúsculas y minúsculas: $numero <> $NUMERO

        PHP tiene un tipado débil, es decir las variables no necesitan ser declaradas antes de ser usadas.

        El nombre de las variables solo pueden llevar caracteres alfanuméricos y no pueden llevar espacios.
        
        Podemos usar varios estilos de escritura:

            Snake Case: $mi_variable

            Upper Camel Case: $MiVariable

            Lower Camel Case: $miVariable

            Kebah: $mi-variable            

*/

